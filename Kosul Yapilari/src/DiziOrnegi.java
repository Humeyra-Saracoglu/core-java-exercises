public class DiziOrnegi {
    public static void main(String[] args) {
        // Amaç: Dizilerin temel yapısını ve elemanlara erişimi öğrenmek

        // 5 elemanlı bir int dizisi oluştur
        int[] sayilar = {10, 20, 30, 40, 50};

        // for döngüsü ile dizinin elemanlarını yazdır
        for (int i = 0; i < sayilar.length; i++) {
            System.out.println("Dizi[" + i + "] = " + sayilar[i]);
        }
    }
}
