public class ArttırmaVeAzaltma {
    public static void main(String[] args) {

        // Arttırma : ++    -----> 1 arttırmak için kullanılan bir operatördür
        // Azaltma : --     -----> 1 azaltmak için kullanılır

        int a = 5;

        a--;
        a--;
        a++;

        System.out.println(a);
    }
}
