public class TemelVeriTipleri {
    public static void main(String[] args) {

                // Tam sayılar (int)
                int yas = 23;

                // Ondalıklı sayılar (double, float)
                double boy = 1.68;
                float agirlik = 59;

                // Karakter (char)
                char cinsiyet = 'K';

                // Mantıksal değer (boolean)
                boolean ogrenciMi = true;

                // Metin (String)
                String isim = "Helin Hümeyra";

                // Sabit değer (final)
                final double PI = 3.14159;

                // Ekrana yazdırma
                System.out.println("İsim: " + isim);
                System.out.println("Yaş: " + yas);
                System.out.println("Boy: " + boy + " m");
                System.out.println("Ağırlık: " + agirlik + " kg");
                System.out.println("Cinsiyet: " + cinsiyet);
                System.out.println("Öğrenci mi?:  " + ogrenciMi);
                System.out.println("Pi sayısı (sabit): " + PI);

    }
}
